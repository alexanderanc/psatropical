let currentLang = "pt";
let studies = [];

const field = {
  id: "ID",
  title: "Título do Artigo",
  authors: "Autor(es)",
  year: "Ano de Publicação",
  doi: "DOI/Link",
  country: "País",
  program: "Nome do PSA",
  impact: "Impacto Reportado",
  magnitude: "Magnitude do Impacto Reportado",
  bias: "Risco de Viés Geral"
};

function t(key) {
  const value = translations[currentLang][key];
  return typeof value === "function" ? value : value || key;
}

function applyLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang === "pt" ? "pt-BR" : "en";
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key] && typeof translations[lang][key] !== "function") {
      el.textContent = translations[lang][key];
    }
  });

  document.getElementById("btn-pt").classList.toggle("active", lang === "pt");
  document.getElementById("btn-en").classList.toggle("active", lang === "en");
  document.getElementById("search").placeholder = translations[lang].searchPlaceholder;

  render();
}

function clean(value) {
  return (value ?? "").toString().trim();
}

function truncate(value, max = 95) {
  const text = clean(value);
  return text.length > max ? `${text.slice(0, max)}…` : text;
}

function linkFromDoi(value) {
  const doi = clean(value);
  if (!doi) return "";
  const url = doi.startsWith("http") ? doi : `https://doi.org/${doi}`;
  return `<a href="${url}" target="_blank" rel="noopener">${truncate(doi, 36)}</a>`;
}

function countBy(items, key) {
  return items.reduce((acc, item) => {
    const value = clean(item[key]) || "NÃO INFORMADO";
    acc[value] = (acc[value] || 0) + 1;
    return acc;
  }, {});
}

function uniqueSorted(items, key) {
  return [...new Set(items.map(item => clean(item[key])).filter(Boolean))]
    .sort((a, b) => a.localeCompare(b, currentLang === "pt" ? "pt-BR" : "en"));
}

function populateFilters() {
  const countrySelect = document.getElementById("country-filter");
  const magnitudeSelect = document.getElementById("magnitude-filter");
  const biasSelect = document.getElementById("bias-filter");

  uniqueSorted(studies, field.country).forEach(country => {
    const opt = document.createElement("option");
    opt.value = country;
    opt.textContent = country;
    countrySelect.appendChild(opt);
  });

  uniqueSorted(studies, field.magnitude).forEach(magnitude => {
    const opt = document.createElement("option");
    opt.value = magnitude;
    opt.textContent = magnitude;
    magnitudeSelect.appendChild(opt);
  });

  uniqueSorted(studies, field.bias).forEach(bias => {
    const opt = document.createElement("option");
    opt.value = bias;
    opt.textContent = bias;
    biasSelect.appendChild(opt);
  });
}

function filteredStudies() {
  const query = clean(document.getElementById("search").value).toLowerCase();
  const country = document.getElementById("country-filter").value;
  const magnitude = document.getElementById("magnitude-filter").value;
  const bias = document.getElementById("bias-filter").value;

  return studies.filter(item => {
    const matchesQuery = !query || clean(item._search).includes(query);
    const matchesCountry = !country || clean(item[field.country]) === country;
    const matchesMagnitude = !magnitude || clean(item[field.magnitude]) === magnitude;
    const matchesBias = !bias || clean(item[field.bias]) === bias;
    return matchesQuery && matchesCountry && matchesMagnitude && matchesBias;
  });
}

function renderStats() {
  const years = studies.map(item => Number(item[field.year])).filter(Boolean);
  const countries = new Set(studies.map(item => clean(item[field.country])).filter(Boolean));
  document.getElementById("stat-articles").textContent = studies.length;
  document.getElementById("stat-countries").textContent = countries.size;
  document.getElementById("stat-years").textContent = `${Math.min(...years)}–${Math.max(...years)}`;
  document.getElementById("stat-vars").textContent = "53";
}

function renderCountryBars(items) {
  const container = document.getElementById("country-bars");
  const counts = Object.entries(countBy(items, field.country))
    .sort((a, b) => b[1] - a[1])
    .slice(0, 8);
  const max = counts.length ? counts[0][1] : 1;
  container.innerHTML = counts.map(([country, count]) => `
    <div class="bar-row">
      <span title="${country}">${truncate(country, 18)}</span>
      <div class="bar-track"><div class="bar-fill" style="width:${(count / max) * 100}%"></div></div>
      <strong>${count}</strong>
    </div>
  `).join("");
}

function renderMagnitude(items) {
  const container = document.getElementById("magnitude-list");
  const counts = Object.entries(countBy(items, field.magnitude)).sort((a, b) => b[1] - a[1]);
  container.innerHTML = counts.map(([label, count]) => `<span class="pill">${label}<span>${count}</span></span>`).join("");
}

function renderTable(items) {
  const tbody = document.getElementById("study-table");
  const preview = items.slice(0, 25);
  if (preview.length === 0) {
    tbody.innerHTML = `<tr><td colspan="8">${t("noResults")}</td></tr>`;
    return;
  }

  tbody.innerHTML = preview.map(item => `
    <tr>
      <td>${clean(item[field.year])}</td>
      <td>
        <div class="article-title">${truncate(item[field.title], 120)}</div>
        <div class="authors">${truncate(item[field.authors], 95)}</div>
      </td>
      <td>${clean(item[field.country])}</td>
      <td>${truncate(item[field.program], 85)}</td>
      <td><span class="badge">${clean(item[field.impact])}</span></td>
      <td><span class="badge">${clean(item[field.magnitude])}</span></td>
      <td><span class="badge">${clean(item[field.bias])}</span></td>
      <td>${linkFromDoi(item[field.doi])}</td>
    </tr>
  `).join("");
}

function render() {
  if (!studies.length) return;
  const items = filteredStudies();
  renderStats();
  renderCountryBars(items);
  renderMagnitude(items);
  renderTable(items);
  document.getElementById("result-count").textContent = t("resultCount")(Math.min(items.length, 25), items.length);
}

async function init() {
  document.getElementById("btn-pt").addEventListener("click", () => applyLanguage("pt"));
  document.getElementById("btn-en").addEventListener("click", () => applyLanguage("en"));

  ["search", "country-filter", "magnitude-filter", "bias-filter"].forEach(id => {
    document.getElementById(id).addEventListener("input", render);
    document.getElementById(id).addEventListener("change", render);
  });

  try {
    const response = await fetch("data/estudos-resumo.json");
    studies = await response.json();
    populateFilters();
    applyLanguage("pt");
  } catch (error) {
    console.error(error);
    document.getElementById("study-table").innerHTML = `<tr><td colspan="8">Erro ao carregar o arquivo data/estudos-resumo.json.</td></tr>`;
  }
}

init();
